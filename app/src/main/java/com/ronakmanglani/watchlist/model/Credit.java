package com.ronakmanglani.watchlist.model;

public class Credit {

    // Attributes
    public String id;
    public String name;
    public String role;
    public String imagePath;

    // Constructors
    public Credit(String id, String name, String role, String imagePath) {
        this.id = id;
        this.name = name;
        this.role = role;
        this.imagePath = imagePath;
    }
}
